    private void workReturnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_workReturnMouseClicked
        if (!workReturnID.getText().isEmpty() && !workReturnDate.getText().isEmpty() && !workReturnWeight.getText().isEmpty() && !workReturnItemWeight.getText().isEmpty()) {
            try {
                int work_id = Integer.parseInt(workReturnID.getText());
                String work_complete_date = workReturnDate.getText();
                float return_wt = Float.parseFloat(workReturnWeight.getText());
                float return_item_wt = Float.parseFloat(workReturnItemWeight.getText());
                boolean pay_to_worker = workReturnPayYes.isSelected();
                int payment_money = Integer.parseInt(workReturnWorkerPaymentMoney.getText());
                float payment_metal = Float.parseFloat(workReturnWorkerPaymentMetel.getText());
                String details[] = work.getDetails(workReturnID.getText());
                if (details[6].equals("0")) {
                    int res = work.complete(work_id, work_complete_date, return_wt, return_item_wt, pay_to_worker, payment_money, payment_metal);
                    error.SQLError(res);
                } else {
                    error.alreadyCompleted();
                }
            } catch (NullPointerException ex) {
                System.out.println("jewelleryshop.Work.workReturnMouseClicked(): " + ex.getMessage());
            }
        } else {
            error.SomethingMissing();
        }
    }//GEN-LAST:event_workReturnMouseClicked

    private void gpSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gpSaveMouseClicked
        if (!gpID.getText().isEmpty() && !gpDate.getText().isEmpty() && !gpWeight.getText().isEmpty() && !gpRefurnish_id.getText().isEmpty()) {
            int id = Integer.parseInt(gpID.getText());
            String Date = gpDate.getText();
            float Weight = Float.parseFloat(gpWeight.getText());
            String metal = gpMetal.getSelectedItem().toString();
            String item = gpItem.getSelectedItem().toString();
            int refurnish_id = Integer.parseInt(gpRefurnish_id.getText());
            String notes = gpNote.getText();

            if (work.isValidRefurnish(refurnish_id)) {
                int ret = work.insert(id, 0, item, "Refurnish", refurnish_id, Date, metal, Weight, notes);
                error.SQLError(ret);
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Refurnish ID#.", "Error...", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            error.SomethingMissing();
        }
    }//GEN-LAST:event_gpSaveMouseClicked


    private void jewelWorkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jewelWorkMouseClicked
        if (!newWorkWorkerID.getText().isEmpty() && !newWorkID.getText().isEmpty() && !newDate.getText().isEmpty() && !newWorkMetalGiven.getText().isEmpty()) {
            int worker_id = Integer.parseInt(newWorkWorkerID.getText());
            int work_id = Integer.parseInt(newWorkID.getText());
            String date_start = newDate.getText();
            String work_type = newWorkType.getSelectedItem().toString();
            int type_id = newWorkTypeID.getText().isEmpty() ? 0 : Integer.parseInt(newWorkTypeID.getText());
            String item_name = newItemname.getSelectedItem().toString();
            String work_metal = newMetal.getSelectedItem().toString();
            float metal_given = Float.parseFloat(newWorkMetalGiven.getText());
            String notes = newWorkNotes.getText();
            if (work.isValidWorker(worker_id)) {
                int ret;
                switch (work_type) {
                    case "Order":
                        if (work.isValidOrder(type_id)) {
                            ret = work.insert(work_id, worker_id, item_name, work_type, type_id, date_start, work_metal, metal_given, notes);
                            error.SQLError(ret);
                        } else {
                            JOptionPane.showMessageDialog(this, "Invalid Order ID#.", "Error...", JOptionPane.ERROR_MESSAGE);
                        }
                        break;
                    case "Refurnish":
                        if (work.isValidRefurnish(type_id)) {
                            ret = work.insert(work_id, worker_id, item_name, work_type, type_id, date_start, work_metal, metal_given, notes);
                            error.SQLError(ret);
                        } else {
                            JOptionPane.showMessageDialog(this, "Invalid Refurnish ID#.", "Error...", JOptionPane.ERROR_MESSAGE);
                        }
                        break;
                    default:
                        ret = work.insert(work_id, worker_id, item_name, work_type, 0, date_start, work_metal, metal_given, notes);
                        error.SQLError(ret);
                        break;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Not a valid worker or worker dose nor exist.", "Error...", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            error.SomethingMissing();
        }
    }//GEN-LAST:event_jewelWorkMouseClicked

